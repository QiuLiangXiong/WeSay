import UIKit

extension UIColor {
    class func mainColor()->UIColor{
        return  UIColor(red: 68/255, green: 123/255, blue: 220/255, alpha: 1.0)
    }
    class func secondColor()->UIColor{
        return UIColor.grayColor()
    }
    class func textColor()->UIColor{
        return UIColor.whiteColor()
    }
    
    
}